            <div class="info-section">
                <h3>MENGAPA EPSS DILAKUKAN?</h3>
                <ul>
                    <li>Mengukur capaian kemajuan penyelenggaraan statistik sektoral pada Instansi Pusat dan Pemerintahan Daerah.</li>
                    <li>Meningkatkan kualitas penyelenggaraan statistik sektoral pada Instansi Pusat dan Pemerintahan Daerah.</li>
                    <li>Meningkatkan kualitas pelayanan publik di bidang statistik pada Instansi Pusat dan Pemerintahan Daerah.</li>
                </ul>
            </div>
            
            <hr class="info-divider">

            <div class="info-section">
                <h3>DIMANA EPSS DILAKSANAKAN?</h3>
                <div class="location-grid">
                    <div class="location-card">
                        <h4>Instansi Pusat</h4>
                        <p>Kementerian, lembaga pemerintah nonkementerian, kesekretariatan negara, kesekretariatan lembaga non struktural, dan lembaga pemerintah lainnya.</p>
                    </div>
                    <div class="location-card">
                        <h4>Pemerintahan Daerah</h4>
                        <p>Provinsi, Kabupaten, Kota.</p>
                    </div>
                </div>
            </div>

            <hr class="info-divider">

            <div class="info-section">
                <h3>BAGAIMANA EPSS DILAKUKAN?</h3>
                <ul>
                    <li><b>Penilaian Mandiri</b> dengan menilai tingkat kematangan dari masing-masing indikator, memberikan penjelasan yang diperlukan, dan menyampaikan bukti dukung oleh Tim Penilai Internal.</li>
                    <li><b>Penilaian Dokumen</b> dengan verifikasi tingkat kematangan hasil penilaian mandiri berdasarkan bukti dukung yang disampaikan oleh Tim Penilai Badan.</li>
                    <li><b>Penilaian Interviu</b> yang dilakukan dengan metode tanya jawab antara Tim Penilai Badan dengan Tim Penilai Internal untuk meyakinkan validitas bukti dukung.</li>
                    <li><b>Penilaian Visitasi</b> yang dilakukan langsung oleh Tim Penilai Badan untuk validasi hasil penilaian mandiri.</li>
                </ul>
            </div>

